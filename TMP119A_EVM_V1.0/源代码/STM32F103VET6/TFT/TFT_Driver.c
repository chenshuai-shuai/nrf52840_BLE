/**********************************************************************
* 名称 : TFT_Fill(uint16_t xsta,uint16_t ysta,uint16_t xend,uint16_t yend,uint16_t color)
* 功能 : TFT在指定区域填充颜色
* 输入 : xsta,ysta   起始坐标
         xend,yend   终止坐标
		 color       要填充的颜色
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_Fill(uint16_t xsta,uint16_t ysta,uint16_t xend,uint16_t yend,uint16_t color)
{          
	uint16_t i = 0x00,j = 0x00; 
	
	TFT_Address_Set(xsta,ysta,xend-1,yend-1);	//设置显示范围
	for(i = ysta;i < yend;i ++)
	{													   	 	
		for(j = xsta;j < xend;j ++)
		{
			TFT_Write_Data(_2BYTE,color);
		}
	} 					  	    
}
/**********************************************************************
* 名称 : TFT_DrawPoint(uint16_t x,uint16_t y,uint16_t color)
* 功能 : 在指定位置画点
* 输入 : x,y 画点坐标
		 color 点的颜色
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_DrawPoint(uint16_t x,uint16_t y,uint16_t color)
{
	TFT_Address_Set(x,y,x,y);					//设置光标位置 
	TFT_Write_Data(_2BYTE,color);
} 
/**********************************************************************
* 名称 : TFT_DrawLine(uint16_t x1,uint16_t y1,uint16_t x2,uint16_t y2,uint16_t color)
* 功能 : 画线
* 输入 : x1,y1   起始坐标
		 x2,y2   终止坐标
		 color   线的颜色
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_DrawLine(uint16_t x1,uint16_t y1,uint16_t x2,uint16_t y2,uint16_t color)
{
	uint16_t t = 0; 
	int xerr = 0,yerr = 0,delta_x,delta_y,distance;
	int incx,incy,uRow,uCol;
	
	delta_x = x2 - x1; 							//计算坐标增量 
	delta_y = y2 - y1;
	uRow = x1;uCol = y1;						//画线起点坐标
	
	if(delta_x > 0)	incx = 1;					//设置单步方向 
	else if (delta_x == 0)	incx = 0;			//垂直线 
	else {incx =-1;delta_x =-delta_x;}
	if(delta_y > 0)	incy = 1;
	else if (delta_y==0) incy = 0;				//水平线 
	else {incy=-1;delta_y=-delta_y;}
	if(delta_x > delta_y)distance = delta_x; 	//选取基本增量坐标轴 
	else distance = delta_y;
	for(t = 0;t < distance + 1;t ++)
	{
		TFT_DrawPoint(uRow,uCol,color);			//画点
		xerr += delta_x;
		yerr += delta_y;
		if(xerr > distance)
		{
			xerr-=distance;
			uRow+=incx;
		}
		if(yerr > distance)
		{
			yerr -= distance;
			uCol += incy;
		}
	}
}
/**********************************************************************
* 名称 : TFT_DrawRectangle(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2,uint16_t color)
* 功能 : 画矩形
* 输入 : x1,y1   起始坐标
		 x2,y2   终止坐标
		 color   矩形的颜色
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_DrawRectangle(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2,uint16_t color)
{
	TFT_DrawLine(x1,y1,x2,y1,color);
	TFT_DrawLine(x1,y1,x1,y2,color);
	TFT_DrawLine(x1,y2,x2,y2,color);
	TFT_DrawLine(x2,y1,x2,y2,color);
}
/**********************************************************************
* 名称 : TFT_Draw_Circle(uint16_t x0,uint16_t y0,uint8_t r,uint16_t color)
* 功能 : 画圆
* 输入 : x0,y0   圆心坐标
		 r       半径
		 color   圆的颜色
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_Draw_Circle(uint16_t x0,uint16_t y0,uint8_t r,uint16_t color)
{
	int a = 0,b = 0;
	a = 0;b = r;	  
	while(a <= b)
	{
		TFT_DrawPoint(x0-b,y0-a,color);        	//3           
		TFT_DrawPoint(x0+b,y0-a,color);     	//0           
		TFT_DrawPoint(x0-a,y0+b,color);    		//1                
		TFT_DrawPoint(x0-a,y0-b,color);   		//2             
		TFT_DrawPoint(x0+b,y0+a,color);      	//4               
		TFT_DrawPoint(x0+a,y0-b,color);       	//5
		TFT_DrawPoint(x0+a,y0+b,color);   		//6 
		TFT_DrawPoint(x0-b,y0+a,color);   		//7
		a ++;
		if((a * a + b * b) > (r * r))			//判断要画的点是否过远
		{
			b--;
		}
	}
}
/**********************************************************************
* 名称 : TFT_ShowChinese(uint16_t x,uint16_t y,uint8_t *s,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode)
* 功能 : 显示汉字串
* 输入 : x,y显示坐标
		 *s 要显示的汉字串
		 fc 字的颜色
		 bc 字的背景色
		 sizey 字号 可选 16 24 32
		 mode:  0非叠加模式  1叠加模式
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_ShowChinese(uint16_t x,uint16_t y,uint8_t *s,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode)
{
	while(*s != 0)
	{
		if(sizey == 12)		 	TFT_ShowChinese12x12(x,y,s,fc,bc,sizey,mode);
		else if(sizey == 16) 	TFT_ShowChinese16x16(x,y,s,fc,bc,sizey,mode);
		else if(sizey == 24) 	TFT_ShowChinese24x24(x,y,s,fc,bc,sizey,mode);
		else if(sizey == 32) 	TFT_ShowChinese32x32(x,y,s,fc,bc,sizey,mode);
		else return;
		s += 2;
		x += sizey;
	}
}
/**********************************************************************
* 名称 : TFT_ShowChinese12x12(uint16_t x,uint16_t y,uint8_t *s,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode)
* 功能 : 显示单个12x12汉字
* 输入 : x,y显示坐标
		 *s 要显示的汉字
		 fc 字的颜色
		 bc 字的背景色
		 sizey 字号
		 mode:  0非叠加模式  1叠加模式
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_ShowChinese12x12(uint16_t x,uint16_t y,uint8_t *s,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode)
{
	uint8_t i = 0,j = 0,m = 0;
	uint16_t k = 0;
	uint16_t HZnum = 0;							//汉字数目
	uint16_t TypefaceNum = 0;					//一个字符所占字节大小
	uint16_t x0 = x;
	TypefaceNum = (sizey / 8 + ((sizey % 8) ? 1 : 0)) * sizey;
	                         
	HZnum = sizeof(tfont12) / sizeof(typFNT_GB12);		//统计汉字数目
	for(k = 0;k < HZnum;k ++) 
	{
		if((tfont12[k].Index[0]==*(s))&&(tfont12[k].Index[1]==*(s+1)))
		{ 	
			TFT_Address_Set(x,y,x+sizey-1,y+sizey-1);
			for(i = 0;i < TypefaceNum;i ++)
			{
				for(j = 0;j < 8;j ++)
				{	
					if(!mode)					//非叠加方式
					{
						if(tfont12[k].Msk[i]&(0x01<<j))TFT_Write_Data(_2BYTE,fc);
						else TFT_Write_Data(_2BYTE,bc);
						m ++;
						if(m % sizey == 0)
						{
							m = 0;	break;
						}
					}
					else						//叠加方式
					{
						if(tfont12[k].Msk[i]&(0x01<<j))	TFT_DrawPoint(x,y,fc);		//画一个点
						x ++;
						if((x - x0) == sizey)
						{
							x = x0;y ++;	break;
						}
					}
				}
			}
		}				  	
		continue;  								//查找到对应点阵字库立即退出，防止多个汉字重复取模带来影响
	}
} 
