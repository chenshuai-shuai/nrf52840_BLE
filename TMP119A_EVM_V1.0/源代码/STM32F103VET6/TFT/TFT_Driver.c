/**********************************************************************
* 名称 : TFT_ShowInt(uint16_t x, uint16_t y, int32_t num, uint8_t show_zero,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode) 
* 功能 : 显示十进制整数
* 输入 : x,y显示坐标
		 num 要显示的数据
		 show_zero 末尾是否显示0？(0-不显示，1-显示)
		 fc 字的颜色
		 bc 字的背景色
		 sizey 字号(12,16,24,32)
		 mode:  0非叠加模式  1叠加模式
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_ShowInt(uint16_t x, uint16_t y, int32_t num, uint8_t show_zero,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode) 
{
    uint8_t buffer[12]; 						// 足够存放"-2147483648\0"
    uint8_t *p = buffer + sizeof(buffer) - 1;
    *p = '\0';
    
    if (num == 0 && !show_zero) 
	{
        *--p = '0';
    } 
	else 
	{
        uint8_t is_negative = (num < 0);
        uint32_t abs_num = is_negative ? -num : num;
        
        while (abs_num > 0 || (num == 0 && show_zero)) 
		{
            *--p = (abs_num % 10) + '0';
            abs_num /= 10;
        }
        
        if (is_negative) 
		{
            *--p = '-';
        }
    }  
    TFT_ShowString(x, y, p, fc, bc, sizey,mode);
}
/**********************************************************************
* 名称 : TFT_ShowDouble(uint16_t x, uint16_t y, double num, uint8_t dp_len, uint8_t show_zero,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode) 
* 功能 : 浮点数显示函数
* 输入 : x,y显示坐标
		 num 要显示的数据
		 dp_len 小数点位数
		 show_zero 末尾是否显示0？(0-不显示，1-显示)
		 fc 字的颜色
		 bc 字的背景色
		 sizey 字号(12,16,24,32)
		 mode:  0非叠加模式  1叠加模式
* 输出 ：无
* 说明 : 无
***********************************************************************/
void TFT_ShowDouble(uint16_t x, uint16_t y, double num, uint8_t dp_len, uint8_t show_zero,uint16_t fc,uint16_t bc,uint8_t sizey,uint8_t mode) 
{
	char buffer[30];
    int len;
    
    //1.先转换为字符串
    len = snprintf(buffer, sizeof(buffer), "%.*f", dp_len, num);
    
    //2.手动处理末尾零
    if (!show_zero && dp_len > 0) {
        char *dot = strchr(buffer, '.');
        if (dot) {
            char *p = buffer + len - 1;
            while (p > dot && *p == '0') *p-- = '\0';
            if (p == dot) *dot = '\0';
        }
    }
	TFT_ShowString(x, y, (uint8_t*)buffer, fc, bc, sizey, mode); 
}

